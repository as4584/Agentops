class ApproveRequest(BaseModel):
    reason: Optional[str] = None  # optional note — not used for approval


class RejectRequest(BaseModel):
    reason: str = ""


@router.patch("/ideas/{job_id}/approve")
async def approve_idea(job_id: str, body: ApproveRequest = ApproveRequest()) -> dict:
    """
    Greenlight an idea → IDEA_APPROVED.
    ScriptWriterAgent will pick it up on the next pipeline run
    and produce a full conflict-arc script + 8 prompt frames.
    """
    try:
        job = get_pipeline().approve_idea(job_id)
    except (KeyError, ValueError) as e:
        raise HTTPException(400, str(e))
    logger.info(f"[Route] Idea {job_id} approved → ScriptWriterAgent queued")
    return {"job_id": job_id, "status": job.status, "message": "Idea approved — script will be generated on next run."}


@router.patch("/ideas/{job_id}/reject")
async def reject_idea(job_id: str, body: RejectRequest = RejectRequest()) -> dict:
    """Reject an idea. Moves to FAILED."""
    try:
        job = get_pipeline().reject_idea(job_id, body.reason)
    except (KeyError, ValueError) as e:
        raise HTTPException(400, str(e))
    return {"job_id": job_id, "status": job.status}


# ── Script + frames review ─────────────────────────────────────────────────────

@router.get("/jobs/{job_id}/frames")
async def get_prompt_frames(job_id: str) -> dict:
    """
    Return the 8 visual prompt frames generated by ScriptWriterAgent.
    Review these before the video generation step.
    Frames are ordered: Hook, Rising Action, Conflict, Comeback 1,
    Second Rising, Second Conflict, Final Comeback, Payoff.
    """
    job = job_store.load(job_id)
    if not job:
        raise HTTPException(404, f"Job {job_id} not found")
    arc_labels = [
        "Hook", "Rising Action", "Conflict", "Comeback 1",
        "Second Rising", "Second Conflict", "Final Comeback", "Payoff",
    ]
    frames_with_labels = [
        {"beat": arc_labels[i] if i < len(arc_labels) else f"Frame {i+1}", "prompt": p}
        for i, p in enumerate(job.prompt_frames)
    ]
    return {
        "job_id": job_id,
        "topic": job.topic,
        "script": job.script,
        "frames": frames_with_labels,
    }


