# ── Upload timing ──────────────────────────────────────────────────────────────

@router.get("/upload-window")
async def check_upload_window() -> dict:
    """
    Check whether the upload timing window is open using Jack Craig's rules:
    - Last video views/hr < VIEW_VELOCITY_THRESHOLD for VIEW_VELOCITY_WINDOW_HOURS
    - At least UPLOAD_INTERVAL_HOURS since last post
    Returns the next recommended upload time.
    """
    from backend.content.publisher_agent import PublisherAgent
    from backend.llm import OllamaClient
    pub = PublisherAgent(OllamaClient())
    import os
    velocity_threshold = int(os.getenv("VIEW_VELOCITY_THRESHOLD", "100"))
    velocity_window = int(os.getenv("VIEW_VELOCITY_WINDOW_HOURS", "12"))
    interval_hours = int(os.getenv("UPLOAD_INTERVAL_HOURS", "48"))

    velocity_clear = pub._check_velocity_window(velocity_threshold, velocity_window)
    last_post = pub._get_last_post_time()
    scheduled = pub._calculate_post_time()

    return {
        "upload_window_open": velocity_clear,
        "last_post_time": last_post.isoformat() if last_post else None,
        "next_recommended_upload": scheduled.isoformat(),
        "rules": {
            "min_interval_hours": interval_hours,
            "velocity_threshold_views_per_hr": velocity_threshold,
            "velocity_window_hours": velocity_window,
        },
    }

